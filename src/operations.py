a = 10
b = 3

print("Addition: ", a + b)
print("Subtraction: ", a - b)
print("Multiplication: ", a * b)
print("Exponentiation: ", a ** b)
print("Division: ", a / b)
print("Floor Division: ", a // b)
print("Modulus: ", a % b)

# Shortcuts
a += 1
b -= 1
a *= 2
b /= 2

# PEMDAS (Parentheses, Exponents, Multiplication and Division, Addition and Subtraction)
print("PEMDAS: ", 3 + 4 * 5 / 2 ** 2 - 1)

# Boolean Operations
print(a == b)
print(a != b)
print(a > b)
print(a < b)